/**
 * QUESTION: Awards points for correctly answering specific questions
 * This is a placeholder - actual implementation depends on question structure
 * @param userAnswer - User's answer to the question
 * @param correctAnswer - Correct answer to the question
 */
export function evaluateQuestion(
  userAnswer: string | number | boolean,
  correctAnswer: string | number | boolean
): boolean {
  return userAnswer === correctAnswer;
}
